const Employee = require("./Employee"); //grabs Employee js file

class Engineer extends Employee {
    //pass original parameter of employee to super function
    constructor (name, id, email, github) {
        super(name, id, email);
        this.github = github;
    }
    getRole() {
        return "Engineer";
    }
    getGithub() {
        return this.github;
    }
}

module.exports = Engineer;